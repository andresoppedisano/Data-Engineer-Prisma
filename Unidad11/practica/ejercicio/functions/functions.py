
class calculadora():

        def sumar(x, y):
                """
                Función que toma 2 valores y retorna la suma de los mísmos.
                Args:
                x: Primer valor a sumar
                y: Segundo valor a sumar

                return: x + y
                """

                return x + y


        def restar(x, y):
                """
                Función que toma 2 valores y retorna la resta de los mísmos.
                Args:
                x: Primer valor a restar
                y: Segundo valor a restar

                return: x - y
                """

                return x - y


        def multiplicar(x, y):
                """
                Función que toma 2 valores y retorna la multiplicación de los mísmos.
                Args:
                x: Primer valor a multiplicar
                y: Segundo valor a multiplicar

                return: x * y
                """

                return x * y

        def dividir(x, y):
                """
                Función que toma 2 valores y retorna la división de los mísmos.
                Args:
                x: Primer valor a dividir
                y: Segundo valor a dividir

                return: x / y
                """

                return x / y